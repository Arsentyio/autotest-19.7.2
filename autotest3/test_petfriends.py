def test_delete_all_my_pets():
    """Проверяем возможность удаления всех питомцев"""

    # Получаем ключ auth_key и запрашиваем список своих питомцев
    _, auth_key = pf.get_api_key(valid_email, valid_password)
    _, my_pets = pf.get_list_of_pets(auth_key, "my_pets")

    # создаем цикл, в котором прописываем условия, если количество питомцев больше, чем 0,
    # то удаляем питомца под №0, до тех пор, пока количество будет равно нулю
    while len(my_pets['pets']) > 0:
        pet_id = my_pets['pets'][0]['id']
        _, my_pets = pf.get_list_of_pets(auth_key, "my_pets")
        status, _ = pf.delete_pet(auth_key, pet_id)
        if len(my_pets['pets']) == 0:
            break
    # Ещё раз запрашиваем список своих питомцев
    _, my_pets = pf.get_list_of_pets(auth_key, "my_pets")

    # Проверяем что статус ответа равен 200 и в списке питомцев нет id удалённого питомца
    assert status == 200
    assert pet_id not in my_pets.values()

def test_add_up_to_5_pets_with_valid_data(name = 'Котяра', animal_type= 'кошатина',
                                     age='5', pet_photo='tests/images/download.jpeg'):
    """Проверяем что можно добавить питомцев с корректными данными, чтоб их количество было не менее 5"""

    # Получаем полный путь изображения питомца и сохраняем в переменную pet_photo
    pet_photo = os.path.join(os.path.dirname(__file__), pet_photo)

    # Запрашиваем ключ api и сохраняем в переменую auth_key
    _, auth_key = pf.get_api_key(valid_email, valid_password)
    _, my_pets = pf.get_list_of_pets(auth_key, "my_pets")

    # Создаем цикл, в котором есть условие, если количество питомцев менее 5,
    # от будем создавать по одному, пока их не станет 5
    # если их уже 5 и более, то цикл не создается

    while True:
        if len(my_pets['pets']) < 4:
            _, my_pets = pf.get_list_of_pets(auth_key, "my_pets")
            status = pf.add_new_pet(auth_key, name, animal_type, age, pet_photo)
            if len(my_pets['pets']) == 4:
               break
            _, my_pets = pf.get_list_of_pets(auth_key, "my_pets")
            assert len(my_pets['pets']) == 5  # Сверяем полученный ответ с ожидаемым результатом
        else:
            break







